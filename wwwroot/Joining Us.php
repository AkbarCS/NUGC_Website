			<h3>How To Get Involved</h3>
		<div class="container-fluid">
			<p>Getting involved with the gliding club is very easy. We can take on new members at any time of the year, there is no requirement to have been with us all year if you wanted to give it a go in April. Flying training is done on an individual basis, so there isn't anything you'll have missed out on, but a lot of training gets done in the first term, so if you want to get to solo standard within a year, it is likely you will need to join us for all 3 terms.</p>
			<b>Joining At Freshers' Fayre</b>
			<p>The majority of our members join us at Freshers' Fayre, at the start of the year. Freshers' Fayre lasts for 3 days, and we will be there all day on all 3 of them. If the weather isn't awful, we will have our 2-seat glider 'NU2' outside on the sports field. We also have a stall inside with some more information, and usually a video playing as well. You can sign up at the stall, and ask questions about the club. If you want to have a sit in the glider, or chat to the committee and more experienced members there about the club, we will be quite happy to answer any questions you have!</p>
			<p>You can collect the gliding club barcode sticker from us at the stall or the glider, which you should then stick to your 'shopping card' along with all the other clubs & societies you want to join. When you have all the stickers of the clubs you want to join, you can pay the membership for them all at once at the tills in the marquee. This will add you to our membership and to our mailing list.</p>
			<b>Joining At Other Times</b>
			<p>Alternatively, you can join us on the Students' Union website. Joining the gliding club from the <a href="https://www.su.nottingham.ac.uk/sports/sport/Gliding/">University of Nottingham Gliding club</a> page. This should add you to our database and mailing list.</p>
			<b>Contacting Us</b>
			<p>If you want to get involved, and want more information, you can contact us to find out anything you need to know. You can also just come along to our socials on Tuesday nights at 7:30, where anyone is welcome and find out more information there. If we aren't making ourselves obvious, look out for the people wearing UoN Gliding fleeces and other clothing!</p>
			<p>Our <a href="https://www.facebook.com/groups/NUGlidingClub/">Facebook page</a> is also open to everyone who would like a bit more information about the club.</p>
