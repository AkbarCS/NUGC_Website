        <div id="accordion-2" class="accordion-section-content">
			<p>Yes, gliding is perfectly safe.</p>
			<p>There are a lot of misconceptions about gliding - the fact that is has no engine worries some people, while others are under the impression that the glider needs wind to fly, and are worried that the wind might stop suddenly. Neither of these points are anything to worry about - lack of an engine doesn't mean the glider can't fly (see the <a href="How gliders fly.php" target="_blank">following</a> page for a detailed explanation), and a glider will fly just as well on a calm day as it will on a day with a breeze.</p>
			<p>In reality, gliding is no more dangerous than driving. Every precaution is taken at Cranwell Gliding Club to ensure the safety of both you and other people. The club is full of very experienced pilots and instructors, and the equipment is maintained to extremely strict standards, and checked every single day before flying.</p>
			<p>In short, there is nothing to worry about.</p>
