        <div id="accordion-5" class="accordion-section-content">
			<p>NUGC organises transport to RAF Cranwell every weekend during term time.</p>
			<p>Any member with a car is welcome to drive people to the airfield for us, and will get their petrol costs paid for by the club.</p>
			<p>We take £5 off each person going flying to cover transport costs, and then pay drivers for each return trip to the airfield.</p>
			<p>Another option for getting to Cranwell is to get the train to either Ancaster or Sleaford station, and then get a lift from someone at the airfield already. Both these stations are only a few miles away from the airfield.</p>
