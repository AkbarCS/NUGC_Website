			<h3>Signups Explained</h3>
			<p>We have had a number of questions regarding the way our signup system works. We created this page to explain the intricacies of the process and hopefully give you a better understanding of it.</p>
			<p>We recently introduced the online signup form which proved to be a success. It has allowed us to have much more control over who gets allocated spaces and for us to optimise allocations. The form is open for 24 hours from Tuesday at 7pm which gives everyone an equal opportunity to signup, even if they are busy on a Tuesday evening.</p>
			<p>Once the form closes, the job of allocating people to their preferred day begins. There are a number of rules and conditions that must be met in order for a trip to be allowed to happen:
				<li>There must be a duty committee member (DCM) attending that day</li>
				<li>There must be enough drivers with cars to transport people</li>
				<li>There must be a good balance of experienced and inexperienced members</li>
				<li>Maximum of 10 members attending each day</li>
			</p>
			<p>Each of these rules influences who does and does not get allocated a space. Occasionally we do not have any DCMs willing to attend, in which case no flying can happen at all that day. The biggest problem we are currently facing is a lack of drivers - we are averaging about 2 out of 4 possible drivers each weekend!</p>
			<p>What we try to do is prioritise people who have been repeatedly signing up and missing out on flying. We understand that in order to progress you need to fly regularly, however we need to give everyone an equal opportunity. Everyone else who does not have priority gets randomly selected and allocated to one of the days they indicated, it is not based on the time you signup. We feel this is the fairest system but we welcome any feedback if you think it can be improved.</p>
			<p>Finally, below is a graph which the flying organiser presented at the 2017 EGM. It shows the number of people signing up (in blue) vs. the number of people who were actually allocated a space (in orange) during the winter semester.</p>
