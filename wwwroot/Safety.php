   <div class="accordion-section">
       <a class="accordion-section-title" href="#accordion-2">The Winch & Cables</a>
        <div id="accordion-2" class="accordion-section-content">
			<p>The airfield is set up with the launch point bus at one end of the field, and the winch at the other end. The winch cables run between them, the entire length of the field. The cables are 4mm steel cable, and are attached to a very powerful engine in the winch. The winch can accelerate a glider from 0 to 60mph in less than 2 seconds in some cases, and the cables are very dangerous to be near when launching gliders.</p>
			<p>Always keep clear of the cables while a launch is in progress. There is a warning buzzer on the bus when launching is taking place, and the lights on the front and sides of the bus will be flashing to signal the winch. 2 cones are set up near the bus - you should stay inside these cones at all times when a glider is being launched.</p>
			<p>Although the 2 winch cables are run independently, it is possible for the to have been crossed, or the winch driver to make a mistake and select the wrong cable for launching. Because of this, you should stay clear of BOTH cables when a launch is taking place, not just the one being used.</p>
			<p>If you want to be shown how to hook the cables to the gliders and launch them, then you can ask someone at the launch point to show you.</p>
			<p>If you are at the winch end of the field, keep well clear of the winch while a launch is in progress - it is recommended you stand at least 15m away, or get inside a vehicle. The cables can land in unexpected places if the glider pilot releases it suddenly.</p>
        </div><!--end .accordion-section-content-->
    </div><!--end .accordion-section-->
    
  	<div class="accordion-section">
       <a class="accordion-section-title" href="#accordion-3">The Launch Point</a> 
        <div id="accordion-3" class="accordion-section-content">
			<p>The launch point is generally where gliders both launch and land. They will land on both sides of the bus, and are pretty quiet. If you need to cross the airfield, you should ask someone, until you know how the airfield operates enough to get safely across yourself. Gliders will normally circuit around the airfield to land, but they can approach from anywhere in some circumstances. You should check that there are no gliders on approach to land before you cross the field.</p>
			<p>If you are half way across the field and you notice a glider landing, stand still. The pilot will have seen you, and there is nothing worse than trying to avoid someone who is running around trying to avoid you! That said, if it is too late for the pilot to do anything, or it still looks like they are flying towards you, get out of the way quickly!</p>
			<p>When driving on the airfield, the same rules apply - you should always check for gliders and planes landing before crossing the landing area. Aircraft always have right of way over ground vehicles, even aircraft moving around on the ground.</p>
			<p>Although gliders will normally land at the launch point, they can land anywhere on the field, so you should keep a good lookout wherever on the field you are.</p>
			<p>As well as winch launching, we also aerotow at Cranwell, using a powered aircraft, and operate a motorglider. Both of these have propellers on the front, which you should stay well clear of when they are operating. Never hang anything off a propeller on the ground, or attempt to spin it, as it may start if the controls in the cockpit have been left in the wrong position.</p>
        </div><!--end .accordion-section-content-->
    </div><!--end .accordion-section-->
    
	<div class="accordion-section">
       <a class="accordion-section-title" href="#accordion-4">Care Of Equipment</a> 
        <div id="accordion-4" class="accordion-section-content">
			<p>This bit isn't so much for safety, but more for taking care of the equipment we have so we don't break anything.</p>
			<p>The speed limit on the airfield for club vehicles is 20mph - this is to avoid breaking the springs, which are expensive and time consuming to replace. Slow down even further for the bumpier parts of the field.</p>
			<p>Never leave the canopy of a glider open and unattended - on a windy day it can slam shut and smash. Even on a calm day, the tug or something driving by can cause a gust of wind, and it is good to get into the habit of doing it anyway!</p>
			<p>Be careful with the parachutes. Always do the leg straps up before the chest strap - again, on a windy day, if you accidently deploy the parachute, you won't want to be dragged down the airfield by your neck. Never put a parachute on the ground - they can absorb moisture which may cause the canopy to stick together. There is a place on the bus you can put unneeded parachutes. Treat them with a bit of respect - it is almost unheard of for one to be used, but they are a piece of life saving equipment nonetheless.</p>
        </div><!--end .accordion-section-content-->
