			<h3>About Us</h3>
			<p>University of Nottingham Gliding Club is one of the oldest clubs in the university, with around 100 or more members joining us every year to fly, from complete beginners to those who have already gone solo elsewhere and want to continue flying. Some of our members have gone on to become national, or even international gliding champions, including our founding member, Brian Spreckley.</p>
			<p>Founded in the late 1970s, the aim of the club is to provide flying training and experience to members as cheaply as possible, regardless of whether someone has flown before. UoN Gliding owns 2 gliders for the use of our members - a two seater and a single seaters, which enables us to train people, and have something for them to progress onto once they are solo and are looking for flights to count towards their badges.</p>
			<p>We are affiliated with Cranwell Gliding Club, who flies from RAF Cranwell in Lincolnshire and who are themselves affiliated with the RAF Gliding & Soaring Association. It is very much a club run by the members, for the members; in other words, this club is staffed by volunteers. Flying at Cranwell is one of the cheapest ways in the country to get airborne and joining the club while you are at university is a fantastic chance to experience something you might not normally get to do.</p>
			<p>We also run several expeditions each year, which any member is welcome to join us on, including our annual Easter trip to Portmoak in Scotland, the Inter-University competition in the summer and various weekends to other clubs as well. We are also extremely active in the university gliding community, attending several inter university events throughout the year. Previous trips have seen us meet Edinburgh at Sutton Bank in Yorkshire for their fireworks party in November, spend New Year at the Long Mynd in Shropshire, and visit local clubs for the annual inter club league competition where we compete for and support our host club, Cranwell.</p>
			<p>UoN Gliding is also a very active club socially - we meet every Tuesday during term time at Portland building on University Park. A lot of socials are organised on other days during term time as well, previous events including ice skating, cinema trips, laser quest, paint balling, BBQs and trips to theme parks.</p>
			<p>This section of the site should tell you a bit more about the club and what we do; information about our gliders as well as Cranwell's, which we have access to; some information about Cranwell Gliding Club, the airfield we fly at; and finally some pricing information.</p>
