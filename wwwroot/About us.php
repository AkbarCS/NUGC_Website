			<h3>About Us</h3>
			<p>University of Nottingham Gliding Club is one of the oldest clubs in the university, with around 100 or more members joining us every year to fly, from complete beginners to those who have already gone solo elsewhere and want to continue flying. Some of our members have gone on to become national, or even international gliding champions, including our founding member, Brian Spreckley.</p>
			<p>Founded in the late 1970s, the aim of the club is to provide flying training and experience to members as cheaply as possible, whatever their experience even none.</p>
			<p>We are affiliated with Cranwell Gliding Club, who flies from RAF Cranwell in Lincolnshire and who are themselves affiliated with the RAF Gliding & Soaring Association. It is very much a club run by the members, for the members - the aim is to have lots of fun, learn and experience some new things and hopefully make lots of new friends. Flying at Cranwell is one of the cheapest ways in the country to get airborne and joining the club while you are at university is a fantastic chance to experience something you might not normally get to do.</p>
			<p>UoN Gliding owns 2 gliders for the use of our members - a two seater and a single seaters, which enables us to train people, and have something for them to progress onto once they are solo and are looking for flights to count towards their badges.</p>
